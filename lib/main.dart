class Glass {
  List<Glasses>? drinks;

  Glass({this.drinks});

  Glass.fromJson(Map<String, dynamic> json) {
    if (json['drinks'] != null) {
      drinks = <Glasses>[];
      json['drinks'].forEach((v) {
        drinks!.add(Glasses.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (drinks != null) {
      data['drinks'] = drinks!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

